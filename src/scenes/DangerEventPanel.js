const MSGS = {
  match:   ['¡Sigue así!', '¡Eso me ayuda!', '¡Bien hecho!', '¡Un poco más!'],
  booster: ['¡Eso me ayudó mucho!', '¡Qué potencia!', '¡Impulso brutal!'],
  danger:  ['¡Rápido, se acerca!', '¡Cuidado, apura!', '¡No te pares!'],
  win:     '¡Me salvaste! 🎉',
  lose:    '¡El tiburón me alcanzó…',
};
const pick = src => Array.isArray(src) ? src[Math.floor(Math.random() * src.length)] : src;

/* ── Panel class ──────────────────────────────────────────────── */

export class DangerEventPanel {
  constructor() {
    this.element = null;
    this._dist   = 40;
  }

  createIn(container) {
    this.element = document.createElement('div');
    this.element.className = 'dep';
    this.element.innerHTML = `
      <div class="dep-scene" id="dep-scene">
        <div class="dep-sky"></div>
        <div class="dep-sun"></div>
        <div class="dep-ocean">
          <div class="dep-wl dep-wl1"></div>
          <div class="dep-wl dep-wl2"></div>
          <div class="dep-wl dep-wl3"></div>
          <div class="dep-wl dep-wl4"></div>
          <div class="dep-reflect"></div>
        </div>
        <div class="dep-shore"></div>
        <div class="dep-palm"><span class="dep-palm-trunk"></span><span class="dep-palm-fronds"></span></div>
        <div class="dep-bubble dep-bub1"></div>
        <div class="dep-bubble dep-bub2"></div>
        <div class="dep-bubble dep-bub3"></div>
        <div class="dep-bubble dep-bub4"></div>
        <div class="dep-foam dep-fa"></div>
        <div class="dep-foam dep-fb"></div>
        <div class="dep-foam dep-fc"></div>
        <div class="dep-foam dep-fd"></div>
        <div class="dep-particles" id="dep-particles"></div>
        <div class="dep-overlay" id="dep-overlay"></div>
        <div class="dep-shark-wrap" id="dep-shark">
          <div class="dep-shark-wake"></div>
          <span class="dep-shark-bub dep-sb1"></span>
          <span class="dep-shark-bub dep-sb2"></span>
          <span class="dep-shark-bub dep-sb3"></span>
          ${SVG_SHARK}
        </div>
        <div class="dep-trexo-wrap" id="dep-trexo">
          <div class="dep-wake"></div>
          <div class="dep-trexo-glow" id="dep-trexo-glow"></div>
          ${SVG_TREXO}
        </div>
        <div class="dep-msg" id="dep-msg">¡El tiburón me sigue!</div>
      </div>
      <div class="dep-bar-wrap">
        <div class="dep-bar-fill" id="dep-bar"></div>
      </div>
    `;
    container.appendChild(this.element);
    this._apply(false);
  }

  onMatch()    { this._move(+8,   pick(MSGS.match),   'match');   }
  onBooster()  { this._move(+18,  pick(MSGS.booster), 'booster'); }
  onLowMoves() { this._move(-12,  pick(MSGS.danger),  false);     }
  onWin()      { this._moveTo(100, MSGS.win,  'win');  }
  onLose()     { this._moveTo(0,   MSGS.lose, 'lose'); }

  onRocket()    { this._speedTrail(); this._move(+22, '¡Velocidad Rex! 🚀',       'booster'); }
  onBomb()      { this._bombWave();        this._move(+15, '¡Boom jurásico! 💥',   'booster'); }
  onColorBomb() { this._rainbowGlow(); this._rainbowSceneFlash(); this._rainbowBoost(); this._stunShark(); this._move(+18, '¡Energía Rex Arcoíris! 🌈', 'booster'); }
  onPtero()     { this._spawnPteroHelper(); this._move(+14, '¡Ataque aéreo! 🦅',   'match'); }
  onMegaCombo() { this._bombWave(); this._flashScene(); this._move(+30, '¡Súper combo! 🔥',   'booster'); }

  // Super-combos: rainbow+rainbow and rainbow+bomb
  onJurassicTotal() {
    this._rainbowGlow(); this._rainbowSceneFlash(); this._rainbowBoost();
    this._flashScene(); this._stunShark();
    this._move(+40, '¡Poder jurásico total! 🌈', 'booster');
  }
  onRainbowBomb() {
    this._rainbowGlow(); this._bombWave(); this._flashScene(); this._speedTrail();
    this._move(+26, '¡Lluvia explosiva Rex! 💥🌈', 'booster');
  }
  onRainbowRocket() {
    this._rainbowGlow(); this._speedTrail(); this._flashScene();
    this._move(+28, '¡Lluvia de cohetes Rex! 🚀🌈', 'booster');
  }
  onBandada() {
    this._rainbowGlow(); this._spawnPteroHelper(); this._spawnPteroHelper();
    this._move(+26, '¡Bandada Rex! 🦅', 'booster');
  }

  // Safe dispatcher — maps a booster type to its scene reaction
  reactToBooster(type) {
    switch (type) {
      case 'color-bomb': case 'rainbow':           return this.onColorBomb();
      case 'bomb':                                  return this.onBomb();
      case 'rocket-h': case 'rocket-v': case 'rocket': return this.onRocket();
      case 'ptero': case 'flying': case 'eagle':    return this.onPtero();
      case 'combo': case 'mega':                    return this.onMegaCombo();
      default:                                      return this.onBooster();
    }
  }

  onTimePressure(timePct) {
    if (!this.element) return;
    // Shark creeps closer as time runs out
    if (timePct < 0.5) {
      const delta = timePct < 0.17 ? -2.2 : timePct < 0.33 ? -1.0 : -0.45;
      this._dist  = Math.max(0, this._dist + delta);
      this._apply(false);
    }
    // Danger overlay follows time pressure
    const overlay = this.element.querySelector('#dep-overlay');
    const scene   = this.element.querySelector('#dep-scene');
    const shark   = this.element.querySelector('#dep-shark');
    if (!overlay?.classList.contains('dep-overlay--win')) {
      overlay?.classList.toggle('dep-overlay--danger', timePct < 0.30 || this._dist < 30);
      scene?.classList.toggle('dep-scene--tense', timePct < 0.17);
      // Aggressive shark + red edge glow when time is nearly out
      const critical = timePct < 0.17 || this._dist < 18;
      scene?.classList.toggle('dep-scene--danger-edge', critical);
      shark?.classList.toggle('dep-shark--attack', critical);
    }
  }

  // Force the scene into a fixed state for quick visual testing (debug only)
  debugState(state) {
    if (!this.element) return;
    const map = {
      normal:  () => { this._dist = 42; this._apply(false); this._setMsg('¡El tiburón me sigue!'); },
      medium:  () => { this._dist = 28; this._apply(false); this.onTimePressure(0.4); this._setMsg('¡Se acerca!', 'urgent'); },
      danger:  () => { this._dist = 14; this._apply(false); this.onTimePressure(0.1); this._setMsg('¡Corre T-REXo!', 'urgent'); },
      rainbow: () => this.onColorBomb(),
      bomb:    () => this.onBomb(),
      rocket:  () => this.onRocket(),
      flying:  () => this.onPtero(),
      combo:   () => this.onMegaCombo(),
      victory: () => this.onWin(),
      fail:    () => this.onLose(),
    };
    (map[state] || map.normal)();
  }

  setMessage(msg, state = false) { this._setMsg(msg, state); }

  _move(delta, msg, fx) {
    this._dist = Math.max(0, Math.min(100, this._dist + delta));
    this._apply(true, fx);
    this._setMsg(msg, fx === 'booster' ? 'boost' : false);
  }

  _moveTo(target, msg, state) {
    this._dist = target;
    this._apply(true, state === 'win' ? 'booster' : false);
    this._setMsg(msg);
    this._setOverlay(state);
  }

  _apply(animate, fx = false) {
    if (!this.element) return;
    const shark   = this.element.querySelector('#dep-shark');
    const trexo   = this.element.querySelector('#dep-trexo');
    const bar     = this.element.querySelector('#dep-bar');
    const scene   = this.element.querySelector('#dep-scene');
    const overlay = this.element.querySelector('#dep-overlay');
    const d       = this._dist;

    if (shark) {
      shark.style.left = `${2 + (100 - d) * 0.30}%`;
      shark.classList.toggle('dep-shark--close', d < 30);
    }
    if (trexo) {
      trexo.style.left = `${50 + d * 0.13}%`;
      if (animate) {
        const burst = fx === 'booster' ? 'dep-trexo--boost' : 'dep-trexo--burst';
        trexo.classList.add(burst);
        setTimeout(() => trexo?.classList.remove(burst), fx === 'booster' ? 480 : 380);
      }
    }
    if (bar) bar.style.width = `${d}%`;
    if (overlay && !overlay.classList.contains('dep-overlay--win'))
      overlay.classList.toggle('dep-overlay--danger', d < 30);
    if (scene) {
      scene.classList.toggle('dep-scene--tense', d < 20);
    }

    if (fx === 'booster') { this._spawnParticles(); this._spawnSpeedLines(1); }
    else if (fx === 'match') this._spawnSpeedLines(0.35);
  }

  _setOverlay(state) {
    const el    = this.element?.querySelector('#dep-overlay');
    const scene = this.element?.querySelector('#dep-scene');
    if (!el) return;
    el.classList.remove('dep-overlay--danger', 'dep-overlay--win');
    if (scene) { scene.classList.remove('dep-scene--tense'); scene.classList.toggle('dep-scene--win', state === 'win'); }
    if (state) el.classList.add(`dep-overlay--${state}`);
  }

  _spawnParticles() {
    const host    = this.element?.querySelector('#dep-particles');
    const trexoEl = this.element?.querySelector('#dep-trexo');
    if (!host || !trexoEl) return;
    const bx = parseFloat(trexoEl.style.left || '55');
    for (let i = 0; i < 7; i++) {
      const p = document.createElement('div');
      p.className = 'dep-particle';
      const a = -Math.PI * 0.5 + (Math.random() - 0.5) * Math.PI * 0.85;
      const r = 14 + Math.random() * 26;
      p.style.left   = `${(bx + (Math.random() - 0.5) * 5).toFixed(1)}%`;
      p.style.bottom = `${(12 + Math.random() * 22).toFixed(0)}px`;
      p.style.animationDelay = `${Math.floor(Math.random() * 90)}ms`;
      p.style.setProperty('--tx', `${Math.round(Math.cos(a) * r)}px`);
      p.style.setProperty('--ty', `${Math.round(Math.sin(a) * r)}px`);
      host.appendChild(p);
      setTimeout(() => p.remove(), 700);
    }
  }

  _spawnSpeedLines(intensity) {
    const host    = this.element?.querySelector('#dep-particles');
    const trexoEl = this.element?.querySelector('#dep-trexo');
    if (!host || !trexoEl) return;
    const bx    = parseFloat(trexoEl.style.left || '55');
    const count = Math.round(2 + intensity * 3);
    for (let i = 0; i < count; i++) {
      const sl = document.createElement('div');
      sl.className = 'dep-speedline';
      sl.style.top  = `${(25 + Math.random() * 45).toFixed(0)}%`;
      sl.style.left = `${(bx - 6 - Math.random() * 14).toFixed(1)}%`;
      sl.style.setProperty('--sl-len', `${Math.round(16 + Math.random() * 28 * intensity)}px`);
      sl.style.animationDelay = `${Math.floor(Math.random() * 80)}ms`;
      host.appendChild(sl);
      setTimeout(() => sl.remove(), 430);
    }
  }

  _bombWave() {
    const shark = this.element?.querySelector('#dep-shark');
    if (!shark) return;
    shark.classList.add('dep-shark--wave');
    setTimeout(() => shark?.classList.remove('dep-shark--wave'), 700);
  }

  // Shark briefly stunned by rainbow energy (wobble + sparkle)
  _stunShark() {
    const shark = this.element?.querySelector('#dep-shark');
    if (!shark) return;
    shark.classList.add('dep-shark--stunned');
    setTimeout(() => shark?.classList.remove('dep-shark--stunned'), 900);
  }

  // Speed reaction: T-REXo dashes with a motion trail (rocket / speed combos)
  _speedTrail() {
    const trexo = this.element?.querySelector('#dep-trexo');
    if (!trexo) return;
    trexo.classList.remove('dep-trexo--speed');
    void trexo.offsetWidth;
    trexo.classList.add('dep-trexo--speed');
    setTimeout(() => trexo?.classList.remove('dep-trexo--speed'), 520);
    this._spawnSpeedLines(1.2);
  }

  _rainbowGlow() {
    const glow = this.element?.querySelector('#dep-trexo-glow');
    if (!glow) return;
    glow.classList.remove('dep-glow--active');
    void glow.offsetWidth;
    glow.classList.add('dep-glow--active');
    setTimeout(() => glow?.classList.remove('dep-glow--active'), 700);
  }

  _rainbowSceneFlash() {
    const overlay = this.element?.querySelector('#dep-overlay');
    if (!overlay) return;
    overlay.classList.remove('dep-overlay--rainbow');
    void overlay.offsetWidth;
    overlay.classList.add('dep-overlay--rainbow');
    setTimeout(() => overlay?.classList.remove('dep-overlay--rainbow'), 650);
  }

  _rainbowBoost() {
    const trexo = this.element?.querySelector('#dep-trexo');
    if (!trexo) return;
    trexo.classList.remove('dep-trexo--rainbow');
    void trexo.offsetWidth;
    trexo.classList.add('dep-trexo--rainbow');
    setTimeout(() => trexo?.classList.remove('dep-trexo--rainbow'), 780);
  }

  _spawnPteroHelper() {
    const host = this.element?.querySelector('#dep-particles');
    if (!host) return;
    const el = document.createElement('div');
    el.className = 'dep-ptero-helper';
    el.textContent = '🦅';
    host.appendChild(el);
    setTimeout(() => el.remove(), 860);
  }

  _flashScene() {
    const overlay = this.element?.querySelector('#dep-overlay');
    if (!overlay) return;
    overlay.classList.add('dep-overlay--flash');
    setTimeout(() => overlay?.classList.remove('dep-overlay--flash'), 400);
  }

  _setMsg(msg, state = false) {
    const el = this.element?.querySelector('#dep-msg');
    if (!el) return;
    el.textContent = msg;
    el.classList.remove('dep-msg--pop', 'dep-msg--urgent', 'dep-msg--boost');
    void el.offsetWidth;
    el.classList.add('dep-msg--pop');
    if (state === 'urgent') el.classList.add('dep-msg--urgent');
    else if (state === 'boost') el.classList.add('dep-msg--boost');
    setTimeout(() => el?.classList.remove('dep-msg--pop'), 400);
  }

  destroy() {
    this.element?.remove();
    this.element = null;
  }
}
